public class NestedIfElse {

    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        if(x==10)
        {
            if(y==20)
            {
                System.out.println("x is 10 and y is 20");
            }
            else
            {
                System.out.println("x is 10 but y is not 20");
            }
            }
            else
            {
                System.out.println("x is not 10");
            }
        }
    }

